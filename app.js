// calculator
const calculator = {
  add: function (a, b) {
    console.log(a + b);
  },
  minus: function (a, b) {
    console.log(a - b);
  },
  divide: function (a, b) {
    console.log(a / b);
  },
  multiply: function (a, b) {
    console.log(a * b);
  },
  power: function (a, b) {
    console.log(a ** b);
  },
};

calculator.add(1, 2);
calculator.minus(1, 2);
calculator.divide(1, 2);
calculator.multiply(1, 2);
calculator.power(1, 2);
